    private void btnCustomerCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerCreateActionPerformed
        if(txtCustomerName.getText().equals("")||txtCustomerPass.getText().equals("")|| boxGender3.getSelectedItem().equals("")|| txtCustomerContactNum.getText().equals("")|| txtCustomerAddress.getText().equals("")|| txtCustomerEmail.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Fill in all Blank Textbox");
        }else{
            String s = "";
            boolean exists = false;
            for (int i = 0; i < tableCustomer.getRowCount(); i++){
                s = tableCustomer.getValueAt(i, 0).toString().trim();
            
                if(txtCustomerName.getText().equals(s)){
                    exists = true;
                    JOptionPane.showMessageDialog(null, "Name Exist, type another Name Please");
                    txtCustomerName.setText("");
                    
                    break;
                }
            }if(exists == false){
            String TechData[] = {txtCustomerName.getText(),txtCustomerPass.getText(),boxGender3.getSelectedItem().toString(),txtCustomerContactNum.getText(),txtCustomerAddress.getText(),txtCustomerEmail.getText(),txtDay.getText()};

            DefaultTableModel table = (DefaultTableModel)tableCustomer.getModel();

            table.addRow(TechData);

            String filePath = "Customer.txt";
            File file = new File(filePath);
            try {
                FileWriter write = new FileWriter(file);
                BufferedWriter buffwrite = new BufferedWriter(write);

                for(int i = 0; i < tableCustomer.getRowCount(); i++){
                    for(int p = 0; p < tableCustomer.getColumnCount(); p++){
                        buffwrite.write(tableCustomer.getValueAt(i, p).toString()+"///");
                    }
                    buffwrite.newLine();
                }

                buffwrite.close();
                write.close();

            } catch (IOException ex) {
                Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this,"Created new Customer Account Successfully");
            txtCustomerName.setText("");
            txtCustomerContactNum.setText("");
            txtCustomerAddress.setText("");
            txtCustomerEmail.setText("");
            
            }
        }
    }//GEN-LAST:event_btnCustomerCreateActionPerformed

    private void tableCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCustomerMouseClicked

        DefaultTableModel table = (DefaultTableModel)tableCustomer.getModel();

        String tableName = table.getValueAt(tableCustomer.getSelectedRow(), 0).toString();
        String tablePass = table.getValueAt(tableCustomer.getSelectedRow(), 1).toString();
        String tableGen = table.getValueAt(tableCustomer.getSelectedRow(), 2).toString();
        for(int i = 0; i < boxGender3.getItemCount(); i++){
            if(boxGender3.getItemAt(i).equalsIgnoreCase(tableGen)){
                boxGender3.setSelectedIndex(i);
            }
        }
        String tableContactNum = table.getValueAt(tableCustomer.getSelectedRow(), 3).toString();
        String tableAddress = table.getValueAt(tableCustomer.getSelectedRow(), 4).toString();
        String tableEmail = table.getValueAt(tableCustomer.getSelectedRow(), 5).toString();
        String tableDay = table.getValueAt(tableCustomer.getSelectedRow(), 6).toString();
        btnCustomerCreate.setEnabled(false);
        txtCustomerName.setText(tableName);
        txtCustomerPass.setText(tablePass);
        txtCustomerContactNum.setText(tableContactNum);
        txtCustomerAddress.setText(tableAddress);
        txtCustomerEmail.setText(tableEmail);
        txtDay.setText(tableDay);
        txtCustomerName.setEnabled(false);
    }//GEN-LAST:event_tableCustomerMouseClicked

