    public void filter (String query){
        DefaultTableModel tb;
        tb = (DefaultTableModel) tableAppointment.getModel();
        TableRowSorter<DefaultTableModel> tablerow = new TableRowSorter<DefaultTableModel>(tb);
        tableAppointment.setRowSorter(tablerow);
        tablerow.setRowFilter(RowFilter.regexFilter(query));
    }
    
    public void filterPay (String query){
        DefaultTableModel tp;
        tp = (DefaultTableModel) tablePayment.getModel();
        TableRowSorter<DefaultTableModel> tablerowp = new TableRowSorter<DefaultTableModel>(tp);
        tablePayment.setRowSorter(tablerowp);
        tablerowp.setRowFilter(RowFilter.regexFilter(query));
    }
    
