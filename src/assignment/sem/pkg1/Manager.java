    private void btnManagerDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManagerDeleteActionPerformed
        DefaultTableModel table = (DefaultTableModel)tableManager.getModel();

        if(tableManager.getSelectedRowCount()==1){
            table.removeRow(tableManager.getSelectedRow());

            String filePath = "Manager.txt";
            File file = new File(filePath);
            try {
                FileWriter write = new FileWriter(file);
                BufferedWriter buffwrite = new BufferedWriter(write);

                for(int i = 0; i < tableManager.getRowCount(); i++){
                    for(int p = 0; p < tableManager.getColumnCount(); p++){
                        buffwrite.write(tableManager.getValueAt(i, p).toString()+"///");
                    }
                    buffwrite.newLine();
                }

                buffwrite.close();
                write.close();

            } catch (IOException ex) {
                Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this, "Successfully removed");
            txtManagerPass.setText("");
            txtManagerName.setText("");
            txtManagerContactNum.setText("");
            txtManagerAddress.setText("");
            txtManagerEmail.setText("");
            txtManagerName.setEnabled(true);
            btnManagerCreate.setEnabled(true);

        }else{
            if(tableManager.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "There are no data left in the table");
            }else{
                JOptionPane.showMessageDialog(this, "Please select a row to remove");
            }
        }
    }//GEN-LAST:event_btnManagerDeleteActionPerformed

    private void btnManagerUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManagerUpdateActionPerformed
        DefaultTableModel table = (DefaultTableModel)tableManager.getModel();
        
        if(txtManagerName.getText().equals("")||txtManagerPass.getText().equals("")|| boxGender.getSelectedItem().equals("")|| txtManagerContactNum.getText().equals("")|| txtManagerAddress.getText().equals("")|| txtManagerEmail.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Fill in all Blank Textbox");
        }else if(tableManager.getSelectedRowCount() == 1){
            String Name = txtManagerName.getText();
            String Password = txtManagerPass.getText();
            String Gen = (String) boxGender.getSelectedItem();
            String ContactNum = txtManagerContactNum.getText();
            String Address = txtManagerAddress.getText();
            String Email = txtManagerEmail.getText();
            String Day = txtDay.getText();

            table.setValueAt(Name, tableManager.getSelectedRow(), 0);
            table.setValueAt(Password, tableManager.getSelectedRow(), 1);
            table.setValueAt(Gen, tableManager.getSelectedRow(), 2);
            table.setValueAt(ContactNum, tableManager.getSelectedRow(), 3);
            table.setValueAt(Address, tableManager.getSelectedRow(), 4);
            table.setValueAt(Email, tableManager.getSelectedRow(), 5);
            table.setValueAt(Day, tableManager.getSelectedRow(), 6);

            JOptionPane.showMessageDialog(this, "Successfully updated!!");

            txtManagerPass.setText("");
            txtManagerName.setText("");
            txtManagerContactNum.setText("");
            txtManagerAddress.setText("");
            txtManagerEmail.setText("");
            btnManagerCreate.setEnabled(true);
            txtManagerName.setEnabled(true);
            tableManager.clearSelection();

            String filePath = "Manager.txt";
            File file = new File(filePath);
            try {
                FileWriter write = new FileWriter(file);
                BufferedWriter buffwrite = new BufferedWriter(write);

                for(int i = 0; i < tableManager.getRowCount(); i++){
                    for(int p = 0; p < tableManager.getColumnCount(); p++){
                        buffwrite.write(tableManager.getValueAt(i, p).toString()+"///");
                    }
                    buffwrite.newLine();
                }

                buffwrite.close();
                write.close();

            } catch (IOException ex) {
                Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
            }

        }else{
            if(table.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "There is no data on the table");
            }else{
                JOptionPane.showMessageDialog(this, "Please select a list for update!");
            }
        }
    }//GEN-LAST:event_btnManagerUpdateActionPerformed

