    private void btnManagerCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnManagerCreateActionPerformed
        if(txtManagerName.getText().equals("")||txtManagerPass.getText().equals("")|| boxGender.getSelectedItem().equals("")|| txtManagerContactNum.getText().equals("")|| txtManagerAddress.getText().equals("")|| txtManagerEmail.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Fill in all Blank Textbox");
        }else{
            String s = "";
            boolean exists = false;
            for (int i = 0; i < tableManager.getRowCount(); i++){
                s = tableManager.getValueAt(i, 0).toString().trim();
            
                if(txtManagerName.getText().equals(s)){
                    exists = true;
                    JOptionPane.showMessageDialog(null, "Name Exist, type another Name Please");
                    txtManagerName.setText("");
                    
                    break;
                }
            }if(exists == false){
            
            
            String TechData[] = {txtManagerName.getText(),txtManagerPass.getText(),boxGender.getSelectedItem().toString(),txtManagerContactNum.getText(),txtManagerAddress.getText(),txtManagerEmail.getText(),txtDay.getText()};

            DefaultTableModel table = (DefaultTableModel)tableManager.getModel();

            table.addRow(TechData);

            String filePath = "Manager.txt";
            File file = new File(filePath);
            try {
                FileWriter write = new FileWriter(file);
                BufferedWriter buffwrite = new BufferedWriter(write);

                for(int i = 0; i < tableManager.getRowCount(); i++){
                    for(int p = 0; p < tableManager.getColumnCount(); p++){
                        buffwrite.write(tableManager.getValueAt(i, p).toString()+"///");
                    }
                    buffwrite.newLine();
                }

                buffwrite.close();
                write.close();

            } catch (IOException ex) {
                Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this,"Created new Manager Account Successfully");
            txtManagerName.setText("");
            txtManagerPass.setText("");
            txtManagerContactNum.setText("");
            txtManagerAddress.setText("");
            txtManagerEmail.setText("");

        }
    }//GEN-LAST:event_btnManagerCreateActionPerformed
    }
    private void tableManagerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableManagerMouseClicked

        DefaultTableModel table = (DefaultTableModel)tableManager.getModel();

        String tableName = table.getValueAt(tableManager.getSelectedRow(), 0).toString();
        String tablePass = table.getValueAt(tableManager.getSelectedRow(), 1).toString();
        String tableGen = table.getValueAt(tableManager.getSelectedRow(), 2).toString();
        for(int i = 0; i < boxGender.getItemCount(); i++){
            if(boxGender.getItemAt(i).equalsIgnoreCase(tableGen)){
                boxGender.setSelectedIndex(i);
            }
        }
        String tableContactNum = table.getValueAt(tableManager.getSelectedRow(), 3).toString();
        String tableAddress = table.getValueAt(tableManager.getSelectedRow(), 4).toString();
        String tableEmail = table.getValueAt(tableManager.getSelectedRow(), 5).toString();
        String tableDay = table.getValueAt(tableManager.getSelectedRow(), 6).toString();
        btnManagerCreate.setEnabled(false);
        txtManagerName.setEnabled(false);
        txtManagerName.setText(tableName);
        txtManagerPass.setText(tablePass);
        txtManagerContactNum.setText(tableContactNum);
        txtManagerAddress.setText(tableAddress);
        txtManagerEmail.setText(tableEmail);
        txtDay.setText(tableDay);
    }//GEN-LAST:event_tableManagerMouseClicked

