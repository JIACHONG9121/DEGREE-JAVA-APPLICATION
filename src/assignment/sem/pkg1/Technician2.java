    private void btnCreate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreate1ActionPerformed
        if(txtTechnicianName1.getText().equals("")||txtTechnicianPass1.getText().equals("")|| boxGender.getSelectedItem().equals("")|| txtContactNum1.getText().equals("")|| txtAddress1.getText().equals("")|| txtEmail1.getText().equals("")|| txtDay.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Fill in all Blank Textbox");
        }else{
            String s = "";
            boolean exists = false;
            for (int i = 0; i < tabletechnician1.getRowCount(); i++){
                s = tabletechnician1.getValueAt(i, 0).toString().trim();
            
                if(txtTechnicianName1.getText().equals(s)){
                    exists = true;
                    JOptionPane.showMessageDialog(null, "Name Exist, type another Name Please");
                    txtTechnicianName1.setText("");
                    
                    break;
                }
            }if(exists == false){
            String TechData[] = {txtTechnicianName1.getText(),txtTechnicianPass1.getText(),boxGender.getSelectedItem().toString(),txtContactNum1.getText(),txtAddress1.getText(),txtEmail1.getText(),txtDay.getText()};

            DefaultTableModel table = (DefaultTableModel)tabletechnician1.getModel();

            table.addRow(TechData);
            
            String filePath = "Technician.txt";
            File file = new File(filePath);
            try {
                FileWriter write = new FileWriter(file);
                BufferedWriter buffwrite = new BufferedWriter(write);
                
                for(int i = 0; i < tabletechnician1.getRowCount(); i++){
                    for(int p = 0; p < tabletechnician1.getColumnCount(); p++){
                        buffwrite.write(tabletechnician1.getValueAt(i, p).toString()+"///");
                    }
                    buffwrite.newLine();
                }
                
                
                buffwrite.close();
                write.close();
                
            } catch (IOException ex) {
                Logger.getLogger(Technician2.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this,"Created new Technician Account Successfully");
            txtTechnicianName1.setText("");
            txtTechnicianPass1.setText("");
            txtContactNum1.setText("");
            txtAddress1.setText("");
            txtEmail1.setText("");
            txtDay.setText("");
        }
    }//GEN-LAST:event_btnCreate1ActionPerformed
    }
