    private void btnDTechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDTechActionPerformed
        
        
        txtPass.setText("");
        txtName.setText("");
        txtContactNum.setText("");
        txtAddress.setText("");
        txtEmail.setText("");
        txtDay.setText("");
        tableView.clearSelection();
        txtSearch.setText(null);
        String search = txtSearch.getText();
        filter(search);
        DefaultTableModel tablemode = (DefaultTableModel)tableView.getModel();
        tablemode.setRowCount(0);
      
        
        String filePath = "Technician.txt";
        File file = new File(filePath);
    
        try {
            FileReader read = new FileReader(file);
            BufferedReader buffread = new BufferedReader(read);
            
            
            Object[] lines = buffread.lines().toArray();
            
            for(int i =0; i < lines.length; i++){
                String[] row = lines[i].toString().split("///");
                tablemode.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Technician2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDTechActionPerformed

