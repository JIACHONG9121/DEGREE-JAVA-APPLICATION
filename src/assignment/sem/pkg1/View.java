    private void btnDCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDCustomerActionPerformed
        txtPass.setText("");
        txtName.setText("");
        txtContactNum.setText("");
        txtAddress.setText("");
        txtEmail.setText("");
        tableView.clearSelection();
        txtSearch.setText("");
        txtDay.setText("");
        String search = txtSearch.getText();
        filter(search);
        DefaultTableModel tablemode = (DefaultTableModel)tableView.getModel();
        tablemode.setRowCount(0);
        
        String filePath = "Customer.txt";
        File file = new File(filePath);
    
        try {
            FileReader read = new FileReader(file);
            BufferedReader buffread = new BufferedReader(read);
            
            
            Object[] lines = buffread.lines().toArray();
            
            for(int i =0; i < lines.length; i++){
                String[] row = lines[i].toString().split("///");
                tablemode.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDCustomerActionPerformed

